number = int(input("Введіть 5-значне число: "))

# Виводимо цифри у зворотному порядку в один рядок
print(
    (number % 10) * 10000 +         # 5-та цифра
    ((number // 10) % 10) * 1000 + # 4-та цифра
    ((number // 100) % 10) * 100 + # 3-тя цифра
    ((number // 1000) % 10) * 10 + # 2-га цифра
    (number // 10000)              # 1-ша цифра
)